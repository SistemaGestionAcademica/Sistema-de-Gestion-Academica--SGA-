    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        // TODO add your handling code here:
        int row = this.gruposTable.getSelectedRow();
        if(row != -1){
            int resp = JOptionPane.showConfirmDialog(this, "Â¿Desea borrar?","Eliminar Grupo",0,0);
            if(resp == JOptionPane.YES_OPTION){
                try {
                    controller.eliminarGrupo(row);
                } catch (GlobalException ex) {
                    Logger.getLogger(GruposView.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NoDataException ex) {
                    Logger.getLogger(GruposView.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(GruposView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_eliminarButtonActionPerformed

