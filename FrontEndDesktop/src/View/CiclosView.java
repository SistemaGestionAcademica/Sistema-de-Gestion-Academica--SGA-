    private void ciclosTBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ciclosTBMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int row = this.ciclosTB.getSelectedRow();
            FrontEndDesktop.CURSO_VIEW.setLocation(evt.getLocationOnScreen());
            controller.editar(row);
        }
    }//GEN-LAST:event_ciclosTBMouseClicked

    private void busFldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_busFldKeyReleased
        // TODO add your handling code here:
        if(RBCodigo.isSelected()){
            try{
                controller.Buscar();
            } catch(GlobalException ex) {
                Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            } catch(NoDataException ex) {
                Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(GruposView.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }else if(RBano.isSelected()){
            try{
                controller.BuscarAno();
            } catch(GlobalException ex) {
                Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            } catch(NoDataException ex) {
                Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(GruposView.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }

    }//GEN-LAST:event_busFldKeyReleased

