    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        int row = this.ciclosTB.getSelectedRow();
        if (row!=-1){
            int resp =  JOptionPane.showConfirmDialog(this, "¿Desea borrar?","Eliminar Ciclo",0,0);
            if (resp== JOptionPane.YES_OPTION){
                try {
                    controller.eliminarCiclo(row);
                } catch (GlobalException ex) {
                    Logger.getLogger(CiclosView.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NoDataException ex) {
                    Logger.getLogger(CiclosView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

