    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        int row = this.alumnosTable.getSelectedRow();
        if (row!=-1){
            int resp =  JOptionPane.showConfirmDialog(this, "Desea borrar?");
            if (resp== JOptionPane.YES_OPTION){
                try {
                    controller.borrar(row);
                } catch (GlobalException ex) {
                    Logger.getLogger(AlumnosView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_eliminarButtonActionPerformed

    private void buscarFldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscarFldKeyReleased

        // TODO add your handling code here:
        if(RBcedula.isSelected()){
            try{
                controller.buscarCedula();
            } catch(GlobalException ex) {
                Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoDataException ex) {  
                        Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            }  
        }else if(RBnombre.isSelected()){
            try{
                controller.buscarNombre();
            } catch(GlobalException ex) {
                Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoDataException ex) {  
                        Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            }  
        }
    }//GEN-LAST:event_buscarFldKeyReleased

