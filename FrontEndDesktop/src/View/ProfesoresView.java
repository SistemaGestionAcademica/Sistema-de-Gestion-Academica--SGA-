    private void buscarFldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscarFldKeyReleased
        // TODO add your handling code here:
        if(RBCedula.isSelected()){
            try{
                controller.buscarProfesor();
            } catch(GlobalException ex) {
                Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoDataException ex) {  
                        Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
            }  
        }else{
            if(RBNombre.isSelected()){
                try{
                    controller.buscarProfesorNombre();
                } catch(GlobalException ex) {
                    Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
                } catch(NoDataException ex) {
                    Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_buscarFldKeyReleased

