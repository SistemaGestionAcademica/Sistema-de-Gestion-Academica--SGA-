    private void profesoresTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_profesoresTableMouseClicked
        if (evt.getClickCount() == 2) 
        {
            int row = this.profesoresTable.getSelectedRow();
            FrontEndDesktop.PROFESOR_VIEW.setLocation(evt.getLocationOnScreen());
            controller.editar(row);
      }
    }//GEN-LAST:event_profesoresTableMouseClicked

    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        int row = this.profesoresTable.getSelectedRow();
        if (row!=-1){
            int resp =  JOptionPane.showConfirmDialog(this, "Desea borrar?");
            if (resp== JOptionPane.YES_OPTION){
                try {
                    controller.eliminarProfesor(row);
                } catch (GlobalException ex) {
                    Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NoDataException ex) {
                    Logger.getLogger(ProfesoresView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_eliminarButtonActionPerformed

